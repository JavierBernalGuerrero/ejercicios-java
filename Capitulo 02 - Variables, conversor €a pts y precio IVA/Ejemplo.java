public class Ejemplo { // Clase principal 
  public static void main(String[] args) {
    int x; // int declara la variable x como entera
    
    x = 5; // Se asigna el valor 5 en la variable x
    System.out.println("El valor actual de mi variable es " + x);
    
    x = 7; // Se asigna el valor 7 en la variable x
    System.out.println("El valor actual de la variable cambia a " + x);
  }
}
